            @click="$emit('remove', hotel)"
            >Remove</my-button
          >
        </div>
      </div>
    </transition>
  </div>
</template>
<script>
import Cookies from "js-cookie"

export default {
  data() {
    return {
      maxSymbol: 300,
    };
  },
  props: {
    hotel: {
      type: Object,
      required: true,
    },
  },
  methods: {
    truncate(hotel) {
      if (hotel.address != null) {
        hotel.address =
          hotel.address.length > this.maxSymbol
            ? `${hotel.address.slice(0, this.maxSymbol - 1)}...`
            : hotel.address;
      }
    },
  },
  mounted() {
    this.truncate(this.hotel);
  },
};
</script>

